static void *DMMTestThread(void *arg)
{
    void *pMem = NULL;
    UINT32 loop = 100;
    UINT32 memLen = 2;
    
    while (loop--)
    {
        pMem = MemPool_Malloc(memLen);
        if (NULL == pMem)
        {
            printf("Malloc Failed:%s:%d\n", __FILE__, __LINE__);
            continue;
        }

        memset(pMem, 0, memLen);
        
        MemPool_Free(pMem);
        //printf("I am:%d loop:%d\n", pthread_self(), loop);
    }
    
    return;
}

