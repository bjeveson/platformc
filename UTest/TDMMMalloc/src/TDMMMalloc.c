static void TDMM_Malloc_Init_Case(void *arg)
{   
    INT32 loop = 0, ret;
    pthread_t pid[TDMM_MALLOC_TEST_THREAD_NUM];
    UINT8 *argv[5];

    argv[0] = (UINT8 *)"dmm";
    argv[1] = (UINT8 *)"8";    
    MemPool_Show(1, argv);        

    for (loop = 0; loop < TDMM_MALLOC_TEST_THREAD_NUM; loop++)
    {
        if ((ret = pthread_create(&pid[loop], NULL, DMMTestThread, NULL)) != 0)
        {
            printf("loop = %d At %s:%d\n",loop, __FILE__, __LINE__);
            perror("");
            
            return;
        }
    }
    
	for (loop = 0; loop < TDMM_MALLOC_TEST_THREAD_NUM; loop++)
	{
		pthread_join(pid[loop], NULL);
    }

    for (loop = 0; loop < 2; loop++)
    {
        sleep(1);
        MemPool_Show(1, argv);
    }
    
    return;
}

