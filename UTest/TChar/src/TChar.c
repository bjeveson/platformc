static void Char_OfIndex_Case(void *Para)
{
    UINT8 *pucStr = "Hello World!\n";
    UINT32 Result;
    
    Result = Char_OfIndex(pucStr, 'd');
    TD_ASSERT_EQUAL(Result, 10);

    Result = Char_OfIndex(pucStr, 'H');
    TD_ASSERT_EQUAL(Result, 0);

    Result = Char_OfIndex(pucStr, '\0');
    TD_ASSERT_EQUAL(Result, 13);

    return;
}

