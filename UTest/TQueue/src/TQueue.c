static void Queue_PushPop_Case(void *Para)
{
    UINT32 loop = 0;
    char *pData = NULL;
    
    for (loop = 0; loop < sizeof(QueueData) / sizeof(QueueData[0]); loop++)
    {
        Queue_Push(g_queue, &QueueData[loop]);
    }

    for (loop = 0; loop < sizeof(QueueData) / sizeof(QueueData[0]); loop++)
    {
        Queue_Pop(g_queue, (void **)&pData);
        TD_ASSERT_EQUAL(*pData, QueueData[loop]);
    }
    
    return;
}

