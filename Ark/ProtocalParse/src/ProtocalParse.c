TProtocalParse ProtocalParse(INT32 Fd, UINT8 *pSocketMsg)
{
    UINT32 ProtocalType = PROTOCAL_TYPE_WEBSOCKET;
    
    switch (ProtocalType)
    {
        case PROTOCAL_TYPE_WEBSOCKET:
        {
            return WS_MsgParse(Fd, pSocketMsg);
        }
        default:
        {
            return;
        }
    }
    
    return;
}

