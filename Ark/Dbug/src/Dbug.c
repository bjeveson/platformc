void DPrintf(UINT8 *ucFileName, 
             UINT32 ulLineNum,
             INT8 *fmt,
             ...)
{
    va_list args;
    UINT8 ucParaStr[1024];
    UINT32 ParaLen;
    
    assert(NULL != fmt);

    va_start(args, fmt);
    ParaLen = vsnprintf((INT8 *)ucParaStr, 
                        1024,
                        (INT8 *)fmt,
                        args);
    va_end(args);

    printf("%s At %s:%d\n", ucParaStr, ucFileName, ulLineNum);
    
    return;
}

