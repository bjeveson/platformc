typedef struct Node *PNode;  

typedef struct Node
{  
    void *data;  
    PNode Next;  
}Node;  
  
typedef struct  
{  
    PNode front;  
    PNode rear;  
    UINT32 size;  
}Queue; 

/*============================================================================*/
/* 类定义                                                                     */
/*============================================================================*/

/*============================================================================*/
/* 模板(template)(包括C++中的类模板和函数模板)                                */
